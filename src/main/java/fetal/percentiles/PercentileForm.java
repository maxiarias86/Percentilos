package fetal.percentiles;

public class PercentileForm {
    private int weeks;
    private int days;
    private double bpd;
    private double hc;
    private double ac;
    private double fl;

    public int getWeeks() {
        return weeks;
    }

    public void setWeeks(int weeks) {
        this.weeks = weeks;
    }

    public int getDays() {
        return days;
    }

    public void setDays(int days) {
        this.days = days;
    }

    public double getBpd() {
        return bpd;
    }

    public void setBpd(double bpd) {
        this.bpd = bpd;
    }

    public double getHc() {
        return hc;
    }

    public void setHc(double hc) {
        this.hc = hc;
    }

    public double getAc() {
        return ac;
    }

    public void setAc(double ac) {
        this.ac = ac;
    }

    public double getFl() {
        return fl;
    }

    public void setFl(double fl) {
        this.fl = fl;
    }
}

